int KeyExpansion(const uint8_t src_key[32], uint8_t key[10][16])
{
	int i, j;
	uint8_t t[16];

	for (i = 0; i < 16; ++i)
	{
		key[0][i] = src_key[16 + i]; // ñòàðøàÿ ÷àñòü êëþ÷à key1_test ÿâëÿåòñÿ ìëàäøèì ðàóíäîâûì êëþ÷îì
		key[1][i] = src_key[i];
	}

	for (i = 0; i < 4; ++i)
	{
		copy_tr(key[(i + 1) * 2], key[i * 2]);
		copy_tr(key[(i + 1) * 2 + 1], key[i * 2 + 1]);
		for (j = 0; j < 8; ++j)
		{
			copy_tr(t, key[(i + 1) * 2 + (j&1)]);
			XSL_transform(C_const[i * 8 + j], t);
			//xor_tr(t, C_const[i * 8 + j]);
			//subst_tr(t);
			//L_transform(t);
			xor_tr(key[(i + 1) * 2 + 1 - (j&1)], t);
		}
	}
	return 0;
}


